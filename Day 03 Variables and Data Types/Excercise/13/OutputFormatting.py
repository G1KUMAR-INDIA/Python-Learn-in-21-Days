# Using f-strings to print a formatted message with a name and age
name = "R JEEVAN KUMAR"
age = 30
print(f"Name: {name}, Age: {age}")

# Using the .format() method to print a formatted message with multiple variables
print("Name: {}, Age: {}".format(name, age))
