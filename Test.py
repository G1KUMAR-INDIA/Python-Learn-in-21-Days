# What is Interpreted and Compiler and its based Languages? 

# Interpreted languages are those that are executed line by line, 
# while compiled languages are those that are compiled into machine code before execution.

# What is OOPs based Language?

# OOPs is a programming paradigm that uses objects 
# and classes to model real-world entities and their interactions.

# What is Memory Management in General and Python?

# Memory management refers to the process of allocating 
# and releasing memory resources for programs.
# Python uses a garbage collector to manage memory automatically, 
# which means you don't need to worry about memory management.

# What is Stack and Heap Memory?

# The stack is a memory area used for storing local variables and function parameters.
# The heap is a memory area used for storing objects and data structures.

# What is Garbage Collector in Python?

# The Python garbage collector automatically manages memory 
# by removing unused objects from memory.
# It detects and frees memory occupied by objects no longer in use, 
# helping prevent memory leaks and optimizing performance.

# What is File Handling?

# File handling refers to the process of working with files, 
# including opening, reading, writing, and closing files.
# Python provides built-in functions for file handling, 
# such as open(), read(), write(), and close().

# What is with Statement?

# The with statement is a context manager in Python, 
# which allows you to work with resources that need to be opened and closed in a specific way.
# It ensures that resources are properly opened and closed, 
# even if an exception occurs during the execution of the block.

